package com.zhskg.bag.enums;

public enum FileTypeEnum {
    IMAGE("图片",0),
    FILE("文件",1);
    private String name;
    private int value;

     FileTypeEnum(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return this.name;
    }

    public int getValue() {
        return this.value;
    }
}
